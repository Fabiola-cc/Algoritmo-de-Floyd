public class Ciudad {
    String nombre;
    String clima_actual;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getClima_actual() {
        return clima_actual;
    }

    public void setClima_actual(String clima_actual) {
        this.clima_actual = clima_actual;
    }

}
